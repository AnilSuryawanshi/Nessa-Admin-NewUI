package com.connecticus.admin.dto;

public class LoginuserListDto {
    	private String fullname;		
		private String uid;		
		private int value;		
		private String firstUsedOn;
		private String lastUsedOn;
		public String getFullname() {
			return fullname;
		}
		public void setFullname(String fullname) {
			this.fullname = fullname;
		}
		public String getUid() {
			return uid;
		}
		public void setUid(String uid) {
			this.uid = uid;
		}
		public int getValue() {
			return value;
		}
		public void setValue(int value) {
			this.value = value;
		}
		public String getFirstUsedOn() {
			return firstUsedOn;
		}
		public void setFirstUsedOn(String firstUsedOn) {
			this.firstUsedOn = firstUsedOn;
		}
		public String getLastUsedOn() {
			return lastUsedOn;
		}
		public void setLastUsedOn(String lastUsedOn) {
			this.lastUsedOn = lastUsedOn;
		}
		
		
		
		
}
