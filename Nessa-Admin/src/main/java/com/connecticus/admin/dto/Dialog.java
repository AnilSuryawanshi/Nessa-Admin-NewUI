package com.connecticus.admin.dto;

public class Dialog {
	
	  private String S;
	  private String U;

	public String getS() {
		return S;
	}

	public void setS(String s) {
		S = s;
	}
	
	

	public String getU() {
		return U;
	}

	public void setU(String u) {
		U = u;
	}

	@Override
	public String toString() {
		return "Dialog [S=" + S + "]";
	}
	  
	  

}
